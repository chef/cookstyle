require 'spec_helper'

describe RuboCop::Cop::Chef::Modernize::DefinesChefSpecMatchers, :config do
  it 'registers an offense when defining a ChefSpec matcher' do
    expect_offense(<<~RUBY)
      if defined?(ChefSpec)
      ^^^^^^^^^^^^^^^^^^^^^ ChefSpec matchers are now auto generated by ChefSpec 7.1+ and do not need to be defined in a cookbook
        def create_yum_repository(resource_name)
          ChefSpec::Matchers::ResourceMatcher.new(:yum_repository, :create, resource_name)
        end
      end
    RUBY

    expect_correction("\n")
  end

  it "doesn't register an offense when checking if another method class is defined" do
    expect_no_offenses(<<~RUBY)
      if defined?(NotChefSpec)
        # something would go here right?
      end
    RUBY
  end
end
